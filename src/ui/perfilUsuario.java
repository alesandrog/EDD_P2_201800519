    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        manejadorUsuarios manejador = new manejadorUsuarios();
        String car = carnet.getText();
        String n = nombre.getText();
        String a = apellido.getText();
        String c = carrera.getText();
        String con = contra.getText();

        if (n.compareTo("") != 0 && a.compareTo("") != 0 && c.compareTo("") != 0 && car.compareTo("") != 0 && con.compareTo("") != 0) {

            try {
                int ca = Integer.parseInt(car);
                if (manejador.registrar(ca, n, a, c, con)) {
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                } else {
                    JOptionPane.showMessageDialog(null, "El usuario ya existe");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Verifique sus datos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

