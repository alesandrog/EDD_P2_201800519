    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        manejadorUsuarios manejador = new manejadorUsuarios();
        String n = nombre.getText();
        String a = apellido.getText();
        String c = carrera.getText();
        String con = contra.getText();

        if (n.compareTo("") == 0) {
            n = SesionActiva.usuario_sesion.getNombre();
        }
        if (a.compareTo("") == 0) {
            a = SesionActiva.usuario_sesion.getApellido();
        }
        if (c.compareTo("") == 0) {
            c = SesionActiva.usuario_sesion.getCarrera();
        }
        if (con.compareTo("") == 0) {
            con = SesionActiva.usuario_sesion.getPassword();
        }

        if (manejador.editar(SesionActiva.usuario_sesion.getCarnet(), n, a, c, con)) {
            JOptionPane.showMessageDialog(null, "Datos Actualizados");
        } else {
            JOptionPane.showMessageDialog(null, "Verifique sus datos");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

