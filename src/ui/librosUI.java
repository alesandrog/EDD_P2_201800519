    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jTable1.setModel(new DefaultTableModel(null, new String[]{"ISBN", "Nombre", "Categoria", "Propietario"}));

        manejadorLibros m = new manejadorLibros();
        m.libro_nombre(jTextField1.getText());
        NodoSimpleB aux = new NodoSimpleB();
        aux = ArbolAvl.resultado_busqueda.inicio.getSiguiente();
        if (aux != null) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            while (aux != null) {
                Vector row = new Vector();
                row.add(aux.getValor().getIsbn());
                row.add(aux.getValor().getTitulo());
                row.add(aux.getValor().getCategoria());
                row.add(aux.getValor().getPropietario());
                model.addRow(row);
                aux = aux.getSiguiente();
            }
            JOptionPane.showMessageDialog(null, "Mostrando coincidencias");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontraron coincidencias");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

